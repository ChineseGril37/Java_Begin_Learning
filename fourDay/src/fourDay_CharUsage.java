//演示char的用法
public class fourDay_CharUsage {
    public static void main(String[] args) {
        char c1 = 'a';
        char c2 = '\t';
        char c3 = '冷';
        char c4 = 97;//说明:字符类型可以直接存放一个数字
        System.out.println(c1);
        System.out.println(c2);
        System.out.println(c3);
        System.out.println(c4);//ascii码 97 = a
    }
}