//变量相当于内存中一个数据存储空间的表示，通过变量名可以访问到变量（值）
public class threeDay_Variety {
    public static void main(String[] args){
        int a = 1;
        //定义了一个变量a，类型int整型，初始赋值为1
        int b = 3;
        //定义了一个变量b，类型int整型，初始赋值为3
        b = 89;
        //把89赋值给b变量
        System.out.println(a);
        //输出a变量的值
        System.out.println(b);
        //输出b变量的值
    }
}
